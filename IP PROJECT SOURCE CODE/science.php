			<p>If the media has taught us anything, it’s that all scientists are crazy. They permanently wear lab coats, have outrageously dishevelled hair and are rather partial to holding a test tube or two. Their jobs only involve doing bizarre and pointless experiments, like cloning sheep, creating monsters with human emotions, or creating crunchy and chocolatey breakfast cereals.

			Yeah, okay, you probably didn’t think that all careers in science are actually like that. However, you might have heard that all scientific careers involve working in silence in laboratories, whilst you inject things into petri dishes, peer through microscopes and check the acidity of various liquids.

