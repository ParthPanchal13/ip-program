		<p>This is a completely baseless and misplaced prejudice and doesn’t hold true when it comes real world situation. Arts stream also has equal challenges and offers good opportunities to the students be it for further studies or career. In fact, for certain professional exams like UPSC Civil Services, the Arts stream subjects that you study during your high-school will help you build a strong foundation that can help you crack the exams in the future.</p>

		<h2>Key Sub-Domains</h2>
		<p>Arts stream is the stream of opportunities. It is one stream in which students have the opportunity to pick the domain that appeal most to them. So, let’s look at some of the key sub-domains of the arts stream.</p>

		<h2>Humanities</h2>
		<p>Humanities is a vast field and defining it within a few words is very difficult. In general, humanities deal with the study and understanding of the ‘human condition’. This includes unearthing the historical background, understanding the reasons behind the evolution of human behaviour, socio-cultural, economic and political systems in the human society. This domain covers a vast array of subjects and the key among them include</p>


		<ul>
			<li>History</li>
			<li>Languages</li>      
			<li>Literature</li>
			<li>Law</li>
			<li>Philosophy</li>
			<li>Religion</li>
			<li>Performing Arts</li>
			<li>Anthropology</li>
			<li>Communication</li>
			<li>Sociology</li>
			<li>Psychology</li>
		</ul>

		<h2>Fine Arts</h2>
		<p>By definition, Fine Arts refer to an art form that is practiced solely for its aesthetic value and beauty. Fine Arts is a general umbrella term that includes Visual Arts, Performing Arts and Literary Arts. As a study stream, it refers to the development of creative skills and thought-process in all the three sub-domains mentioned above.</p>

		<h2>Visual Arts</h2>
		<p>As the name suggests, visual arts is the field that is associated with development of visual art forms. It covers learning about the famous artists and their works and understanding their motivation. In addition to these, it also involves developing one’s creative skills to create their own pieces of visual art i.e. drawing, painting, sculptures and others. The key sub-domains within visual arts stream include</p>

		<ul>
			<li>Painting</li>
			<li>Drawing</li>
			<li>Sculpture</li>
			<li>Photography</li>
			<li>Conceptual arts</li>
			<li>Architecture</li>
			<li>Weaving</li>
			<li>Fabrics</li>
		</ul>
		
		<h2>Performing Arts</h2>
		<p>Performing Arts is also a sub-domain of the fine arts and involves development of performing arts skills in a student. The study of performing arts covers domains such as music, acting, dancing and theatre. Students will be involved in learning about the key techniques and skills required for performing arts. The key study domains within the performing arts stream include:</p>

		<ul>
			<li>Music</li>
