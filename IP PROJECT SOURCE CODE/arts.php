		<h2>Scope of further studies in the Arts stream</h2>

		<p>Among the many factors that students need to consider while picking a stream after Class 10th; one of the most crucial one has to be the scope of further study. While the science and commerce streams have their key subjects as common; the case is quite different when it comes to the Arts stream. For the Arts stream, the plethora of options available for further studies makes it important for the students to pick the right subjects during the high-school level itself. For instance, a someone looking forward to build their career in Cartography, it is important for them to select Geography as one of the electives during high-school. In addition to this, the Arts stream also offers a host courses and training programmes to the students. The key among them are mentioned below</p>

		<h2>Bachelor’s Degree Courses</h2>

		<p>Bachelor of Arts / B.A is most popular undergraduate level course that is available to the students after completion of their high school. The course is offered in a variety of subjects which students can choose as per the career path they want to pursue. Generally, during the first year of the course, students have to pick three key subjects and during the final year, one core subject has to be picked for specialization. The popular Bachelor’s degree courses available for Arts stream students include:</p>

		<ul>
			<li>B.A in History</li>
			<li>B.A in Geography</li>
			<li>B.A in Political Science</li>
			<li>B.A in Public Administration</li>
			<li>B.A in English Literature</li>
			<li>B.A in Economics</li>
		</ul>

		<h2>Technical Courses</h2>

		<p>Just because you picked the Arts stream doesn’t mean that you don’t get to tech oriented courses. There are a few technical courses also available for the Arts stream students at the undergraduate level which can put you on the path to building a bright career in technical domain. The key among them include:</p>

		<ul>
			<li>BCA – Bachelor of Computer Application</li>
			<li>B.Arch – Bachelor of Architecture</li>
		</ul>


		<h2>Law Courses</h2>

		<p>If legal services and law is something that interests you, Law courses at undergraduate level would be an ideal choice for you as an Arts student. Generally, the eligibility to join an LLB programme is a graduate degree; however, nowadays students can opt for a 5 year integrated programmes in law. The key among them include:</p>
		<ul>
			<li>B.A + L.L.B – Bachelor of Arts & Bachelor of Legislative Law</li>
			<li>BBA + L.L.B – Bachelor of Business Administration & Bachelor of Legislative Law</li>
		</ul>

